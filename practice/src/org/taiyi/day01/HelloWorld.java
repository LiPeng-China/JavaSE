package org.taiyi.day01;

// public 类的修饰符
// class 类的关键字
// HelloWorld 类名
// {} 类的范围
public class HelloWorld {
    // public 公共的，修饰符
    // static 代表静态的，一个关键字
    // void 代表没有返回类型
    // main 代表程序入口
    // String[] args 代表数组，程序参数

    public static void main(String[] args) {
        // 输出语句 输出 Hello World!
        System.out.println("Hello World!");
    }
}
