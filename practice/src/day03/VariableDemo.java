public class VariableDemo {
    public static void main(String[] args) {
        // 定义一个3.5 的变量
        double d = 3.5;
        // 重新给变量赋值
        d = 3.6;
        System.out.println(d);

        // 定义变量 张三 的 名字
        String name = "张三";
        System.out.println(name);
        name = "李四";
        System.out.println(name);
    }
}
